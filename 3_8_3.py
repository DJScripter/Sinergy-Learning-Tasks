# Считываем максимальную массу лодки и количество рыбаков
m = int(input())
n = int(input())

# Считываем веса рыбаков
weights = []
for _ in range(n):
    weight = int(input())
    weights.append(weight)

# Сортируем веса по убыванию
weights.sort(reverse=True)

# Считаем минимальное количество лодок
boats = 0
i = 0  # Индекс текущего рыбака

while i < n:
    # Если можем взять двух рыбаков (их веса <= m) и есть ещё хотя бы один рыбак
    if i + 1 < n and weights[i] + weights[i + 1] <= m:
        i += 2  # Отправляем двух рыбаков
    else:
        i += 1  # Отправляем одного рыбака
    boats += 1  # Увеличиваем счётчик лодок

# Выводим результат
print(boats)
